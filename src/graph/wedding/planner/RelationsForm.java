    public void getFormGuests(String[] Guests) {
        
        int counter = 0;
        
        this.guestList.setModel(new DefaultListModel());
        DefaultListModel model = (DefaultListModel)this.guestList.getModel();
        for(String v : Guests) {
            if(v != this.guestComboBox.getSelectedItem()) {
                model.addElement(v);
                counter++;
            }
        }
        this.guestsNames = Guests;
    }
    
    
