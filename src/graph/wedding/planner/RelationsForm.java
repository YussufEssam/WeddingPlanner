    private void setRelationButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_setRelationButtonActionPerformed
        
        Comboselected = this.guestComboBox.getSelectedItem().toString();
        Listselected = this.guestList.getSelectedIndices();
        
        //int temp = 0;
        
        for(int i = 0; i < Listselected.length; i++) {
            g.addEdge(Comboselected, this.guestList.getModel().getElementAt(Listselected[i]));
        }
        
    }//GEN-LAST:event_setRelationButtonActionPerformed

